public class Task4 {
    public static void main(String[] args) {
        int fac = 10;
        for (int i = 1; i <= 10; i++) {
            fac *= i;
        }
        System.out.println(fac);



        int fact = 1;
        int i = 1;
        while (i <= 10) {
            fact *= i;
            i++;
        }
        System.out.println(fact);
}
}

